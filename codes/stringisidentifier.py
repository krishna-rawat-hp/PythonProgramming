# String isidentifier method in python

# Example-1 simple string
str1 = "abc"
print(str1.isidentifier())

# Example-2 string start with digit
str2 = "12Krishna"
print(str2.isidentifier())

# Example-3 string start with underscore(_)
str3 = "_krishna"
print(str3.isidentifier())

# Example-4 string alphanumeric
str4 = "krishna123"
print(str4.isidentifier())

# Example-5 special character
str5 = "abc#"
print(str5.isidentifier())