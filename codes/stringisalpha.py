# String isalpha method in python

# Example-1 with alphabet
str1 = "Krishna"
print(str1.isalpha())

# Example-2 with alpha numeric 
str2 = "Krishna1234"
print(str2.isalpha())

# Example-3 with spaces
str3 = "Krishna Kant Rawat"
print(str3.isalpha())

# Example-4 with special characters
str4 = "Krishna$Kant@Rawat"
print(str4.isalpha())