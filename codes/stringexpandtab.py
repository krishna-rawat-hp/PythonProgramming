# String expandtab method in python

# Example-1 default tab size
s1 = "Krishna \t Kant \t Rawat"
print(s1)
print(s1.expandtabs())

# Example-2 tab size modified 1
print(s1.expandtabs(1))

# Example-3 tab size modified 2
print(s1.expandtabs(2))

# Example-4 tab size modified 3
print(s1.expandtabs(3))
