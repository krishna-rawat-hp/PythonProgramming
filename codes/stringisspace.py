# String isspace method in python

# Example-1 blank white spaced string
str1= " "
print(str1.isspace())

# Example-2 only one space between two strings
str2 = "Krishna Rawat"
print(str2.isspace())

# Example-3 multi space in string
str3 = "krishna Kant rawat"
print(str3.isspace())

# Example-4 \n and \t
str4 = "\n \t \n"
print(str4.isspace())