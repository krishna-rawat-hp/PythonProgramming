# String islower method in python

# Example-1 lowercase string
str1 = "krishna kant rawat"
print(str1.islower())

# Example-2 lowercase string with digit and special character
str2 = "krishna 8319******"
print(str2.islower())

# Example-3 camelcase string and uppercase string
str3 = "Krishna Kant RAWAT"
print(str3.islower())
