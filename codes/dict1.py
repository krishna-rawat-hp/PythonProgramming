# Python dictionary  creating

# Example-1 creating dictionary by {:}
dict1 = {"Name":"Krishna","Age":19,"Nationality":"Indian"}
print(type(dict1))
print("Data:",dict1)
print("\n ========================== \n")

# Example-2 Empty dictionary
dict2 = {}
print(type(dict2))
print(dict2)
print("\n ========================== \n")

# Example-3 using dict() method
dict3 = dict({1:"krishna",2:"kant",3:"rawat"})
print(type(dict3))
print(dict3)
print("\n ========================== \n")

# Example-4 using dict() method with list and tuple
dict4 = dict([(1,"krishna"),(2,"kant"),(3,"rawat")]) 
print(type(dict4))
print(dict4)
