# String lstrip method in python

# Example-1 simple string
str1 = "krishna kant rawat"
print("old string:",str1)
print("new string:",str1.lstrip())

# Example-2 single parameter
str2 = "    krishna kant rawat   "
print("old string:",str2)
print("new string:",str2.lstrip())

# Example-3 double parameter
str3 = "#####-Krishna Kant Rawat-#####"
print("old string:",str3)
print("new string:",str3.lstrip("#"))
