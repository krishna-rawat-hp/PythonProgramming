# Python tuple Declare and initialization

# Example-1 Declare empty tuple
tup1 = ()
print("tup1 data & type: ",tup1,type(tup1))

# Example-2 initialize tuple with comma (Right way)
tup2 = ("Hello",)
print("tup2 data & type: ",tup2,type(tup2))

# Example-3 initialization without comma (Wrong way)
tup3 = ("Hello")
print("tup3 data & type: ",tup3,type(tup3))
