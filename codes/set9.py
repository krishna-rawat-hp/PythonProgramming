# Python set operations

# Example-1 intersection of two sets using & operator
set1 = {"Krishna","Hemant","Raja","Atul","mehta"}
set2 = {"Rahul","Mohan","Krishna","Hemant"}
print("Intersection of two sets (&): ",set1&set2)

# Example-2 Intersection of two sets using intersection() method
print("Intersection of two set (intersection()): ",set1.intersection(set2))
