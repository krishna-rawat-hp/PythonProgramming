# String index method in python

# Example-1 single parameter
str1 = "Hello This is krishna kant rawat"
print(str1.index("is"))

# Example-2 double parameter
print(str1.index("krishna",10))

# Example-3 triple parameter
print(str1.index("kant",22,28))

# Example-4 index not found
print(str1.index("krs"))

