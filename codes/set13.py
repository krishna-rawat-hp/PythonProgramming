# Python set comparision

# Example-1 set comparision with (>)
set1 = {"Krishna","Hemant","Raja","Atul","mehta"}
set2 = {"Krishna","Hemant","Raja"}
set3 = {"Krishna","Hemant","Raja"}
print("Is set1 > set2: ",set1>set2) # True Since set1 is superset of set2

# Example-2 set comparision with(<)
print("Is set1 < set3: ",set1<set3) # False Since set1 is not a subset of set3

# Example-3 set comparision with(==)
print("Is set2 == set3: ",set2==set3) # True Since set2 is equivalant of set3 
