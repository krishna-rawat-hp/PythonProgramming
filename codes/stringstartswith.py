# String startswith method in python
# Example-1 single parameter
str1 = "Hello python"
print(str1.startswith("Hello"))

# Example-2 is not start
print(str1.startswith("python"))

# Example-3 double parameter
print(str1.startswith("py",6))

# Example-4 triple parameter
print(str1.startswith("pyth",6,11))